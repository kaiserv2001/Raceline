/** Standard tyre degradation curve over a stint: soft -3/lap, med -1.8, hard -1. */
const degCurve = (laps: number): TyreDegData[] =>
  Array.from({ length: laps }, (_, i) => {
    const lap = i + 1;
    return {
      lap,
      soft: Math.max(0, Math.round((100 - lap * 3) * 10) / 10),
      medium: Math.max(0, Math.round((100 - lap * 1.8) * 10) / 10),
      hard: Math.max(0, Math.round((100 - lap * 1) * 10) / 10),
    };
  });

const corner = (
  number: number,
  apexSpeed: number,
  brakingDistance: number,
  gear: number,
  name?: string,
): CornerDetail => ({
  id: `c${number}`,
  number,
  name,
  apexSpeed,
  brakingDistance,
  gear,
});

const zone = (id: string, type: Zone["type"], x: number, y: number, cornerId?: string): Zone => ({
  id,
  type,
  x,
  y,
  cornerId,
});

